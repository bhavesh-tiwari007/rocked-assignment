const {DataTypes} = require('sequelize');
const sequelize = require('../db/db');

const Campaign = sequelize.define('rpt_campaigns', {
    id: {
        type: DataTypes.INTEGER,
        autoIncrement: true,
        primaryKey: true
    },
    name: {
        type: DataTypes.STRING,
        allowNull: false
    },
    created_at: {
        type: DataTypes.DATE,
        allowNull: false
    },
    updated_at: {
        type: DataTypes.DATE,
        allowNull: false
    }
});

module.exports = Campaign;